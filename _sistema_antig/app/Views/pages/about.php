            <h2>Nossa Missão</h2>
            <p>O <strong>InfoRagro</strong> nasceu com a missão de democratizar o acesso à informação de qualidade sobre o agronegócio brasileiro. Acreditamos que produtores rurais, técnicos, investidores e todos os profissionais do setor merecem conteúdo confiável, atualizado e prático.</p>
            
